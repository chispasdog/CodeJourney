
-- INNER JOIN en Oracle es una cláusula que se utiliza en una sentencia SELECT para combinar filas de dos o más tablas en base a una condición de unión. Esta cláusula devuelve solo las filas que tienen una coincidencia en ambas tablas.
SELECT columna1, columna2, ...
FROM tabla1
INNER JOIN tabla2
ON tabla1.columna = tabla2.columna;
