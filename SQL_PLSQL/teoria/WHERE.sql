
-- La cláusula WHERE en Oracle se utiliza para filtrar los datos en una consulta SELECT, permitiendo que solo se muestren las filas que cumplan con ciertas condiciones. Se puede utilizar una amplia variedad de operadores y expresiones para definir las condiciones en la cláusula WHERE.
SELECT nombre, apellido, departamento
FROM empleados
WHERE departamento = 'Ventas';
